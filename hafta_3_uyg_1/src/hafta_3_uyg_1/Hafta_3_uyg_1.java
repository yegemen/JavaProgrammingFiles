package hafta_3_uyg_1;

public class Hafta_3_uyg_1 {

    public static void main(String[] args) {

        int a = 3, b = 5;

        System.out.println("TamSayi 1: " + a + " ," + " TamSayi 2:" + b);

        System.out.println("TamSayi 1(" + a + ") + " + "TamSayi 2(" + b + ") = " + (a + b));

        System.out.println("TamSayi 1(" + a + ") - " + "TamSayi 2(" + b + ") = " + (a - b));

        System.out.println("TamSayi 1(" + a + ") * " + "TamSayi 2(" + b + ") = " + (a * b));

        System.out.println("TamSayi 1(" + a + ") / " + "TamSayi 2(" + b + ") = " + (a / b));

        System.out.println("TamSayi 1(" + a + ") % " + "TamSayi 2(" + b + ") = " + (a % b));

    }

}
